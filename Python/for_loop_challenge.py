print('This program will replace all vowels with ib')



s = input('Enter a word or sentence:')




for c in s:
    if c == 'a':
        print('ib' + c, end = '')
    elif c == 'e':
        print('ib' + c, end = '')
    elif c == 'i':
        print('ib' + c, end = '')
    elif c == 'o':
        print('ib' + c, end = '')
    elif c == 'u':
        print('ib' + c, end = '')
    else:
        print(c, end = '')
        
